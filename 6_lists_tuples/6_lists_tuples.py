# Slices:

spam = [1,2,3,4]

# start at index 1, go upto 4 (not including)
print(spam[1:4])

# start at index 1 --> go to the end
print(spam[1:])

# start at 0 (start) --> go until not including index 3
print(spam[:3])

# start at 0 --> go until the LAST index (not including)
print(spam[0:-1])

# start at 0 --> go until not including index 2 [0,1]
print(spam[:2])

